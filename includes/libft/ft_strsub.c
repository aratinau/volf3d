#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char			*str;
	unsigned int	i;

	if (!s)
		return (NULL);
	str = ft_strnew(len);
	if (str == NULL)
		return (NULL);
	i = 0;
	while (i < start)
	{
		s++;
		i++;
	}
	ft_strncpy(str, s, len);
	str[len] = '\0';
	return (str);
}
